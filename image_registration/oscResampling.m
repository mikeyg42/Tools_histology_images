function f = oscResampling(x)
% from the MATLAB website documentation re: kernels for interpolation
% https://www.mathworks.com/help/matlab/creating_plots/create-and-compare-resizing-interpolation-kernels.html
absx = abs(x);
absx2 = absx.^2;

f = (absx <= 1) .* ...
    ((-0.168*absx2 - 0.9129*absx + 1.0808) ./ ...
    (absx2 - 0.8319*absx + 1.0808)) ...
    + ...
    ((1 < absx) & (absx <= 2)) .* ...
    ((0.1953*absx2 - 0.5858*absx + 0.3905) ./ ...
    (absx2 - 2.4402*absx + 1.7676));
end